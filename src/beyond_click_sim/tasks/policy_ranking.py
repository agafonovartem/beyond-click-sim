from __future__ import annotations

from typing import Any

import pandas as pd

from beyond_click_sim.data.canonical import CanonicalDataset
from beyond_click_sim.tasks.base import (
    DatasetFilter,
    ItemFeatureBuilder,
    Splitter,
    Task,
    TaskBuilder,
    TaskSchema,
)
from beyond_click_sim.tasks.policies import Policy
from beyond_click_sim.tasks.samplers import PostSplitUserSampler


class PolicyRankingTaskBuilder(TaskBuilder):
    """Build a policy-ranking task for Q3 evaluation.

    Train contains all observed interactions for scorer fitting. Test contains
    recommendation rows generated by each policy for each evaluation user, with
    real outcome targets derived from held-out test interactions.

    Val is always empty — no threshold selection is needed for Q3.

    The test DataFrame has one row per (user, policy, recommended_item). Beyond
    the standard scorer-visible columns (user_id, item_id, features,
    history_context_columns), each test row also carries:

    - ``policy``:   string name of the recommender policy
    - ``rank``:     integer rank assigned by the policy (1 = most recommended)

    These extra columns are not part of TaskSchema (they are not scorer inputs);
    they are used by the runner to aggregate policy-level utilities.

    ``target`` is 1 if the recommended (user_id, item_id) pair appears in the
    user's held-out test interactions with a positive ``target_source_column``
    value, and 0 otherwise.
    """

    def __init__(
        self,
        name: str,
        dataset_filter: DatasetFilter,
        splitter: Splitter,
        policies: list[Policy],
        *,
        target_source_column: str = "target_interact",
        target_column: str = "target",
        user_column: str = "user_id",
        item_column: str = "item_id",
        history_context_columns: tuple[str, ...] = (),
        eval_sampler: PostSplitUserSampler | None = None,
        item_feature_builder: ItemFeatureBuilder | None = None,
    ) -> None:
        super().__init__(
            name=name,
            target_source_column=target_source_column,
            dataset_filter=dataset_filter,
            splitter=splitter,
            sampler=None,
            target_column=target_column,
            user_column=user_column,
            item_column=item_column,
            sampled_column=None,
            candidate_group_column=None,
            history_context_columns=history_context_columns,
            item_feature_builder=item_feature_builder,
        )
        if not policies:
            raise ValueError("policies must be non-empty.")
        if self.dataset_filter is None:
            raise ValueError("PolicyRankingTaskBuilder requires dataset_filter.")
        if self.splitter is None:
            raise ValueError("PolicyRankingTaskBuilder requires splitter.")
        self.policies = list(policies)
        self.eval_sampler = eval_sampler
        if (
            self.eval_sampler is not None
            and getattr(self.eval_sampler, "max_rows_per_user", None) is not None
        ):
            raise ValueError(
                "PolicyRankingTaskBuilder cannot use a row-capped eval sampler: "
                "policy-ranking target lookup needs all held-out rows for each "
                "selected evaluation user."
            )
        self._validate_history_context_columns()

    def build(self, dataset: CanonicalDataset) -> Task:
        users = dataset.load_users()
        items = dataset.load_items()
        interactions = dataset.load_interactions()

        self._require_columns(interactions, [self.user_column, self.item_column])
        self._require_columns(
            interactions,
            [self.target_source_column, *self.history_context_columns],
        )
        self._require_columns(users, [self.user_column])
        self._require_columns(items, [self.item_column])

        users, items, interactions = self.dataset_filter.filter(
            users=users,
            items=items,
            interactions=interactions,
        )
        split = self.splitter.split(interactions)

        # Cap evaluation users via optional post-split sampler.
        test_interactions, test_eval_summary = self._sample_eval_rows(
            split.test,
            train=split.train,
        )

        # Determine which users to evaluate and build recommendation rows.
        test_users = (
            users[users[self.user_column].isin(
                test_interactions[self.user_column].unique()
            )].copy()
        )
        rec_frames: list[pd.DataFrame] = []
        for policy in self.policies:
            policy.fit(
                split.train,
                items=items,
                user_column=self.user_column,
                item_column=self.item_column,
            )
            recs = policy.recommend(
                test_users,
                train_interactions=split.train,
                items=items,
                user_column=self.user_column,
                item_column=self.item_column,
            )
            rec_frames.append(recs)

        all_recs = pd.concat(rec_frames, ignore_index=True) if rec_frames else pd.DataFrame(
            columns=[self.user_column, self.item_column, "policy", "rank"]
        )

        # Compute real targets: 1 if (user, item) in test interactions with positive label.
        test_targets: dict[tuple[Any, Any], Any] = {}
        for _, row in test_interactions.iterrows():
            key = (row[self.user_column], row[self.item_column])
            test_targets[key] = row[self.target_source_column]

        target_values = [
            test_targets.get(
                (row[self.user_column], row[self.item_column]),
                0,
            )
            for _, row in all_recs.iterrows()
        ]
        all_recs = all_recs.copy()
        all_recs[self.target_column] = target_values

        # Mask history context columns to NA for test rows (no leakage).
        for col in self.history_context_columns:
            all_recs[col] = pd.NA

        # Enrich items with train-only features.
        items, item_feature_manifest = self._enrich_items_from_train(
            items=items,
            train_interactions=split.train,
        )

        user_features = self._prefixed_features(users, self.user_column, "user")
        item_features = self._prefixed_features(items, self.item_column, "item")
        feature_columns = self._feature_columns(
            user_features,
            item_features,
            user_column=self.user_column,
            item_column=self.item_column,
        )

        # Build test: join features onto recommendation rows.
        test_task_columns = self._task_columns(feature_columns) + ["policy", "rank"]
        test = self._with_features(
            rows=all_recs,
            user_features=user_features,
            item_features=item_features,
            columns=test_task_columns,
            user_column=self.user_column,
            item_column=self.item_column,
        )

        # Build train: observed interactions with features (same as regression builder).
        train_rows = self._target_rows(split.train)
        train_task_columns = self._task_columns(feature_columns)
        train = self._with_features(
            rows=train_rows,
            user_features=user_features,
            item_features=item_features,
            columns=train_task_columns,
            user_column=self.user_column,
            item_column=self.item_column,
        )

        # Val is always empty.
        val = pd.DataFrame(columns=train.columns)

        schema = TaskSchema(
            target_column=self.target_column,
            feature_columns=feature_columns,
            id_columns=(self.user_column, self.item_column),
            candidate_group_column=None,
            sampled_column=None,
            history_context_columns=self.history_context_columns,
        )

        return Task(
            name=self.name,
            train=train,
            val=val,
            test=test,
            schema=schema,
            manifest={
                "protocol": "policy_ranking",
                "dataset": dataset.name,
                "dataset_version": dataset.version,
                "target_source_column": self.target_source_column,
                "target_column": self.target_column,
                "feature_columns": list(feature_columns),
                "history_context_columns": list(self.history_context_columns),
                "item_feature_builder": item_feature_manifest,
                "policies": [
                    {
                        "class": policy.__class__.__name__,
                        "name": policy.name,
                        "k": policy.k,
                        "seed": policy.seed,
                    }
                    for policy in self.policies
                ],
                "filter": self._component_manifest(self.dataset_filter),
                "splitter": self._component_manifest(self.splitter),
                "sampler": None,
                "eval_sampler": (
                    None
                    if self.eval_sampler is None
                    else self._component_manifest(self.eval_sampler)
                ),
                "eval_user_selection": {
                    "kind": "post_split",
                    "test": test_eval_summary,
                },
                "rows": {
                    "train": int(len(train)),
                    "val": 0,
                    "test": int(len(test)),
                },
                "users": {
                    "filtered": int(interactions[self.user_column].nunique()),
                    "train": int(split.train[self.user_column].nunique()),
                    "test": int(test_interactions[self.user_column].nunique()),
                },
                "items": int(items[self.item_column].nunique()),
            },
        )

    def _target_rows(self, interactions: pd.DataFrame) -> pd.DataFrame:
        rows = interactions[
            [self.user_column, self.item_column, *self.history_context_columns]
        ].copy()
        rows[self.target_column] = interactions[self.target_source_column]
        return rows

    def _sample_eval_rows(
        self,
        rows: pd.DataFrame,
        *,
        train: pd.DataFrame,
    ) -> tuple[pd.DataFrame, dict[str, int | None]]:
        if self.eval_sampler is not None:
            return self.eval_sampler.sample(rows, train=train)
        if rows.empty:
            return rows.copy(), {
                "eligible_users": 0,
                "selected_users": 0,
                "rows_before": 0,
                "rows_after": 0,
            }
        return rows.copy(), {
            "eligible_users": int(rows[self.user_column].nunique()),
            "selected_users": int(rows[self.user_column].nunique()),
            "rows_before": int(len(rows)),
            "rows_after": int(len(rows)),
        }

    def _task_columns(self, feature_columns: tuple[str, ...]) -> list[str]:
        return [
            self.user_column,
            self.item_column,
            *feature_columns,
            *self.history_context_columns,
            self.target_column,
        ]

    def _validate_history_context_columns(self) -> None:
        reserved = {self.user_column, self.item_column, self.target_column}
        overlaps = [
            col for col in self.history_context_columns if col in reserved
        ]
        if overlaps:
            raise ValueError(
                "history_context_columns cannot include task control columns: "
                f"{overlaps}"
            )
